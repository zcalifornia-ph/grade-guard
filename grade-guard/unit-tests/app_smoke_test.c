void ui_profile_login(Student_Profile* profile)
{
    if (current_scenario) {
        current_scenario->login_called++;
    }

    if (!profile || !profile->courses) {
        return;
    }

    if (current_scenario) {
        current_scenario->course_count_before_login = vector_size(profile->courses);
    }

    if (current_scenario && current_scenario->delete_course_during_login && vector_size(profile->courses) > 0) {
        Course* removed_course = (Course*)vector_remove(profile->courses, 0);

        if (removed_course) {
            course_destroy(removed_course);
        }
    }

    if (current_scenario) {
        current_scenario->course_count_after_login = vector_size(profile->courses);
    }
}

Student_Profile* profile_select_by_student_number(Vector* student_profiles)
{
    Student_Profile* selected_profile;

    if (current_scenario) {
        current_scenario->select_profile_called++;
        current_scenario->observed_profile_count = student_profiles ? vector_size(student_profiles) : 0;
    }

    if (!student_profiles || vector_size(student_profiles) == 0) {
        return NULL;
    }

    selected_profile = (Student_Profile*)vector_at(student_profiles, 0);
    if (current_scenario && current_scenario->expected_student_number && selected_profile && selected_profile->student_number) {
        current_scenario->selected_student_number_matched =
            strcmp(current_scenario->expected_student_number, selected_profile->student_number) == 0;
    }

    return selected_profile;
}

static void test_create_and_exit_round_trip(TestContext* context)
{
    const int menu_selections[] = {0, 2};
    SmokeScenario scenario = {0};
    char runtime_directory[PATH_BUFFER_SIZE];
    char saved_profile_path[PATH_BUFFER_SIZE];
    Student_Profile profile;
    PersistenceStatus status;
    float weighted_average;
    float predicted_gwa;
    int app_status;

    TEST_EXPECT(context, prepare_runtime_directory(runtime_directory, sizeof(runtime_directory), "create-exit"), "create/exit runtime directory should be created");
    if (context->failures != 0) {
        return;
    }

    scenario.menu_selections = menu_selections;
    scenario.menu_selection_count = sizeof(menu_selections) / sizeof(menu_selections[0]);
    app_status = run_app_in_runtime(context, runtime_directory, &scenario);
    TEST_EXPECT(context, app_status == 0, "app_run should succeed for create/exit flow");
    TEST_EXPECT(context, scenario.failure_message == NULL, "create/exit flow should not emit a failure message");
    TEST_EXPECT(context, scenario.create_profile_called == 1, "create/exit flow should create one profile");
    TEST_EXPECT(context, scenario.header_calls == 2, "create/exit flow should render the main header twice");
    TEST_EXPECT(context, scenario.hide_caret_calls == 1, "create/exit flow should initialize the caret once");
    TEST_EXPECT(context, scenario.fullscreen_calls == 1, "create/exit flow should initialize fullscreen once");
    TEST_EXPECT(context, scenario.set_font_size_calls == 1, "create/exit flow should initialize the font once");

    snprintf(saved_profile_path, sizeof(saved_profile_path), "%s/0.csv", runtime_directory);
    status = persistence_load_student_profile(saved_profile_path, &profile);
    TEST_EXPECT(context, status == PERSISTENCE_STATUS_OK, "saved profile should load after create/exit flow");
    if (status != PERSISTENCE_STATUS_OK) {
        return;
    }

    expect_string_equal(context, "Smoke", profile.first_name, "saved profile should preserve first name");
    TEST_EXPECT_SIZE_EQ(context, 1, vector_size(profile.courses), "create/exit flow should persist one course");

    weighted_average = calculate_weighted_average(&profile);
    expect_float_close(context, 0.92f, weighted_average, "create/exit flow should preserve weighted grade data");
    TEST_EXPECT(context, calculate_predicted_gwa(&profile, &predicted_gwa), "create/exit flow should preserve predicted GWA inputs");
    expect_float_close(context, 1.32f, predicted_gwa, "create/exit flow should preserve the expected predicted GWA");
    student_profile_reset(&profile);
}

