static char* duplicate_text(const char* value)
{
    size_t length;
    char* copy;

    if (!value) {
        return NULL;
    }

    length = strlen(value);
    copy = (char*)malloc(length + 1);
    if (!copy) {
        return NULL;
    }

    memcpy(copy, value, length + 1);
    return copy;
}

static float absolute_difference(float left, float right)
{
    float difference = left - right;

    return difference < 0.0f ? -difference : difference;
}

static void expect_float_close(TestContext* context, float expected, float actual, const char* message)
{
    if (absolute_difference(expected, actual) > FLOAT_TOLERANCE) {
        fprintf(
            stderr,
            "FAIL: %s (expected %.3f, got %.3f) (%s:%d)\n",
            message,
            expected,
            actual,
            __FILE__,
            __LINE__
        );
        context->failures++;
    }
}

static void expect_string_equal(TestContext* context, const char* expected, const char* actual, const char* message)
{
    int matches = 0;

    if (!expected && !actual) {
        matches = 1;
    } else if (expected && actual && strcmp(expected, actual) == 0) {
        matches = 1;
    }

    TEST_EXPECT(context, matches, message);
}

static int push_element_checked(Vector* vector, const void* element)
{
    size_t previous_size;

    previous_size = vector_size(vector);
    vector_push_back(vector, element);
    return vector_size(vector) == previous_size + 1;
}

static int add_activity(Course_Parameter* parameter, const char* name, float score, float total_score)
{
    Activities activity;

    if (!parameter || !parameter->activities || !activities_init(&activity)) {
        return 0;
    }

    activity.activity_name = duplicate_text(name);
    if (!activity.activity_name) {
        return 0;
    }

    activity.score = score;
    activity.total_score = total_score;
    if (!push_element_checked(parameter->activities, &activity)) {
        activities_reset(&activity);
        return 0;
    }

    return 1;
}

static int add_parameter(
    Course_Component* component,
    const char* name,
    float weight,
    const ActivitySpec* activities,
    size_t activity_count
)
{
    Course_Parameter parameter;
    size_t i;

    if (!component || !component->parameters || !course_parameter_init(&parameter)) {
        return 0;
    }

    parameter.parameter_name = duplicate_text(name);
    if (!parameter.parameter_name) {
        course_parameter_reset(&parameter);
        return 0;
    }

    parameter.weight = weight;
    parameter.percentage = 0.0f;

    for (i = 0; i < activity_count; i++) {
        if (!add_activity(&parameter, activities[i].name, activities[i].score, activities[i].total_score)) {
            course_parameter_reset(&parameter);
            return 0;
        }
    }

    if (!push_element_checked(component->parameters, &parameter)) {
        course_parameter_reset(&parameter);
        return 0;
    }

    return 1;
}

static int add_course(Student_Profile* profile, Course* course)
{
    if (!profile || !profile->courses || !course) {
        return 0;
    }

    return push_element_checked(profile->courses, course);
}

static int build_smoke_profile(Student_Profile* profile, int include_second_course)
{
    Course first_course;
    Course second_course;
    const ActivitySpec quiz_activities[] = {
        {"Quiz 1", 18.0f, 20.0f},
        {"Quiz 2", 20.0f, 20.0f}
    };
    const ActivitySpec exam_activities[] = {
        {"Final Exam", 90.0f, 100.0f}
    };
    const ActivitySpec performance_activities[] = {
        {"Performance Task", 50.0f, 50.0f}
    };

    if (!student_profile_init(profile)) {
        return 0;
    }

    if (!course_init(&first_course)) {
        student_profile_reset(profile);
        return 0;
    }

    profile->first_name = duplicate_text("Smoke");
    profile->middle_name = duplicate_text("Q");
    profile->last_name = duplicate_text("Tester");
    profile->student_number = duplicate_text("2026-60001");
    profile->degree_program = duplicate_text("BSCS");
    profile->goal = 1.20f;

    first_course.name = duplicate_text("CMSC 11");
    if (!profile->first_name || !profile->middle_name || !profile->last_name ||
        !profile->student_number || !profile->degree_program || !first_course.name) {
        course_reset(&first_course);
        student_profile_reset(profile);
        return 0;
    }

    first_course.units = 3.0f;
    first_course.lab_flag = 0;
    if (!add_parameter(&first_course.lecture, "Quizzes", 40.0f, quiz_activities, 2) ||
        !add_parameter(&first_course.lecture, "Exams", 60.0f, exam_activities, 1) ||
        !add_course(profile, &first_course)) {
        course_reset(&first_course);
        student_profile_reset(profile);
        return 0;
    }

    if (!include_second_course) {
        return 1;
    }

    if (!course_init(&second_course)) {
        student_profile_reset(profile);
        return 0;
    }

    second_course.name = duplicate_text("PE 2");
    if (!second_course.name) {
        course_reset(&second_course);
        student_profile_reset(profile);
        return 0;
    }

    second_course.units = 2.0f;
    second_course.lab_flag = 0;
    if (!add_parameter(&second_course.lecture, "Performance", 100.0f, performance_activities, 1) ||
        !add_course(profile, &second_course)) {
        course_reset(&second_course);
        student_profile_reset(profile);
        return 0;
    }

    return 1;
}

static int make_directory_if_missing(const char* path)
{
    if (MAKE_DIRECTORY(path) == 0) {
        return 1;
    }

    return errno == EEXIST;
}

static int prepare_runtime_directory(char* buffer, size_t buffer_size, const char* scenario_name)
{
    static unsigned int counter = 0;
    unsigned long timestamp = (unsigned long)time(NULL);
    int written;

    if (!make_directory_if_missing("grade-guard/unit-tests/app-smoke-runtime")) {
        return 0;
    }

    counter++;
    written = snprintf(
        buffer,
        buffer_size,
        "grade-guard/unit-tests/app-smoke-runtime/%s-%lu-%u",
        scenario_name,
        timestamp,
        counter
    );
    if (written <= 0 || (size_t)written >= buffer_size) {
        return 0;
    }

    return make_directory_if_missing(buffer);
}

static int run_app_in_runtime(TestContext* context, const char* runtime_directory, SmokeScenario* scenario)
{
    char original_directory[PATH_BUFFER_SIZE];
    int result;

    TEST_EXPECT(context, GET_CURRENT_DIRECTORY(original_directory, sizeof(original_directory)) != NULL, "current directory should be readable");
    if (context->failures != 0) {
        return 1;
    }

    TEST_EXPECT(context, CHANGE_DIRECTORY(runtime_directory) == 0, "runtime directory should be enterable");
    if (context->failures != 0) {
        return 1;
    }

    current_scenario = scenario;
    result = app_run();
    current_scenario = NULL;

    TEST_EXPECT(context, CHANGE_DIRECTORY(original_directory) == 0, "original directory should be restorable");
    return result;
}

